public class Perbandingan {
  public static void main(String[] args){
    int nilai1 = 100;
    int nilai2 =  100;
    System.out.println(nilai1 > nilai2); // false
    System.out.println(nilai1 < nilai2); // false
    System.out.println(nilai1 <= nilai2); // true
    System.out.println(nilai1 >= nilai2); // true
    System.out.println(nilai1 == nilai2); // true
    System.out.println(nilai1 != nilai2); // false
  }
}
